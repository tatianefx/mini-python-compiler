def main() -> None:
	numero = 0

	print("Digite um numero de 1 a 5: ")
	int(input(numero))
	if numero ==1:
		print("Um")
	elif numero == 2:
		print("Dois")
	elif numero ==3:
		print("Tres")
	elif numero ==4:
		print("Quatro")
	elif numero ==5:
		print("Cinco")
	else:
		print("Numero Invalido!!!")

main()
